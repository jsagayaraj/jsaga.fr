				<h2>Barot Bellingham</h2>
				<img class="pull-left img-rounded" src="images/artists/Barot_Bellingham_tn.jpg" alt="Photo of Barot Bellingham">
				<div class="media-body">
					<p>Barot can trace his decision to become an artist to the ripe old age of two, when his mother put his first crayon painting on the refrigerator. From that point on, he was obsessed with art, and began creating mature works of art as early as five years old. A child prodigy, Barot sold his first professional painting at the age of nine for $2000, and was guest lecturing on art by the time he was eleven.</p>
					<p>Barot has just finished his final year at The Royal Academy of Painting and Sculpture, where he excelled in glass etching paintings and portraiture. Hailed as one of the most diverse artists of his generation, Barot is equally as skilled with watercolors as he is with oils, and is just as well-balanced in different subject areas. Barot's collection entitled "The Un-Collection" will adorn the walls of Gilbert Hall, depicting his range of skills and sensibilities - all of them, uniquely Barot, yet undeniably different.</p>
