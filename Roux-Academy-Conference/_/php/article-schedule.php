					<h3>The Use and Absence of Color in Modern Art</h3>
					<h4>Elizabeth Hall</h4>
					<p>Hassum Harrod is a "color specialist," in every sense of the phrase. His inherent ability to create color palettes that illuminate an image and allure an audience, has drawn praise from traditionalists as well as modern artists - no small feat in today's diverse art world. His master's thesis at San Francisco Art Academy is entitled "Color Upon Color – The Mixing and Reinventing of Color." It is this thesis that prompted Hillford & Bilken to publish his solo-authored book on color, that will be used in undergraduate art programs throughout the world starting next year.</p>
					<p>His session at CAC will be an offshoot of his new book and is expected to be standing-room only. So, grab a seat and be prepared for a lesson on color that you'll never forget.</p>
