			  	<h4>10:30 to 11:30 am</h4>
					<h3>The Art of Salvaging</h3>
					<h4>Victoria Hall</h4>
					<p>Never was the phrase "one person's trash is another person's treasure" more accurate than when discussing Constance Olivia Smith's art methodology. While some of her creations are built using fine gemstones, the majority of her work relies upon the discards of a wasteful planet. From bottlecaps to paper scraps to mechanical parts – there is seemingly no refuse that hasn't been used in an original scrap art painting by Olivia. And while there are an increasing number of scrap artists in existence, none have the level of achievement that is so frequently observed in Olivia's creations.</p>
					<p>In the Art of Salvaging, Olivia will share her talent for finding and selecting scrap materials, as well as the philosophy behind several of her more celebrated works of art. If you're interested in recycling and mixed media, this session will include a lively discussion of both, from the woman who Art International Digest calls the "foremost queen of scrap art."</p>
